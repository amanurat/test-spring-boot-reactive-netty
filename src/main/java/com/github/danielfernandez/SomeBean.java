package com.github.danielfernandez;

public class SomeBean {


	private String valuegl;
	private String valuees;

	public SomeBean(final String valuegl, final String valuees) {
		this.valuegl = valuegl;
		this.valuees = valuees;
	}


	public String getValuegl() {
		return this.valuegl;
	}

	public void setValuegl(final String valuegl) {
		this.valuegl = valuegl;
	}

	public String getValuees() {
		return this.valuees;
	}

	public void setValuees(final String valuees) {
		this.valuees = valuees;
	}

}
